package obligatorio_1_ayed1;

/**
 *
 * @author rodrigo
 */
public class Nodo {
    String nombre;
    Nodo Siguiente;

    public Nodo(String nombre) {
        this.nombre = nombre;
        this.Siguiente = null;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Nodo getSiguiente() {
        return Siguiente;
    }

    public void setSiguiente(Nodo Siguiente) {
        this.Siguiente = Siguiente;
    }
     
}
